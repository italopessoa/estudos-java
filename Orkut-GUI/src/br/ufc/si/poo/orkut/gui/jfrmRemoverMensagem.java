    /**
     * @author italoney
     * Preencher lista de mensagens do forum selecionado
     */
    private void preencherMensagens() {
        DefaultListModel dlm = new DefaultListModel();
        for (String mensagem : this.forum.getMensagens()) {
            dlm.addElement(mensagem);
        }
        jlistMensagens.setModel(dlm);
    }
