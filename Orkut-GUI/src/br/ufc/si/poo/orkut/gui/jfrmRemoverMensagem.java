    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        String mensagem = jlistMensagens.getSelectedValue().toString();
        try {
            this.forum.removerMensagem(mensagem);

            int resposta = JOptionPane.showConfirmDialog(null, "Mensagem removida.\nDeseja continuar?",
                    "Sucesso", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (resposta == JOptionPane.YES_OPTION) {
                preencherMensagens();
            } else {
                jfrmPrincipal principal = new jfrmPrincipal();
                this.foruns.put(this.forum.getTitulo(), this.forum);

                for (Comunidade com : this.comunidades.values()) {
                    if (com instanceof ComunidadePrivada) {
                        ComunidadePrivada cp = (ComunidadePrivada) com;
                        cp.removerForum(forum);
                        cp.adicionarForum(forum);
                        this.comunidades.put(cp.getTitulo(), cp);
                    }
                }


                principal.atualizarListas(comunidades, usuarios, foruns);
                principal.show();
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao remover mensagem", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnRemoverActionPerformed

