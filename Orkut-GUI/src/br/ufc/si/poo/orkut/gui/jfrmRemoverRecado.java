    /**
     * @author italoney
     * preencher lista de recados do usuario selecionado
     */
    private void preencherRecados() {
        DefaultListModel dlm = new DefaultListModel();
        UsuarioVip uv = (UsuarioVip) this.usuario;
        for (String mensagem : uv.getRecados()) {
            dlm.addElement(mensagem);
        }
        jlistRecados.setModel(dlm);
    }
