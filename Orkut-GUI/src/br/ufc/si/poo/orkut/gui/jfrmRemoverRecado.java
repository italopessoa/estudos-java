    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        String recado = jlistRecados.getSelectedValue().toString();
        try {

            UsuarioVip uv = (UsuarioVip) this.usuario;
            uv.removerRecado(recado);

            int resposta = JOptionPane.showConfirmDialog(null, "Recado removido.\nDeseja continuar?",
                    "Sucesso", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (resposta == JOptionPane.YES_OPTION) {
                preencherRecados();
            } else {
                jfrmPrincipal principal = new jfrmPrincipal();
                this.usuarios.put(this.usuario.getNome(), this.usuario);

                for (Comunidade com : this.comunidades.values()) {
                    com.removerUsuario(usuario);
                    com.adicionarUsuario(usuario);
                    this.comunidades.put(com.getTitulo(), com);
                }


                principal.atualizarListas(comunidades, usuarios, foruns);
                principal.show();
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao remover recado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

