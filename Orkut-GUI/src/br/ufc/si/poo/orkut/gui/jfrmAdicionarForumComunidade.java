    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        Comunidade c = (Comunidade) jcmbComunidade.getSelectedItem();
        if (c instanceof ComunidadePrivada) {
            ComunidadePrivada cp = (ComunidadePrivada) this.comunidades.get(c.getTitulo());
            if (jlistForum.getSelectedValues().length >= 1) {
                for (Object obj : jlistForum.getSelectedValues()) {
                    Forum f = (Forum) obj;
                    if (cp.contemForum(f)) {
                        JOptionPane.showMessageDialog(null, "O forum: "
                                + f.getTitulo() + " j√° esta na comunidade", "Aten√ß√£o",
                                JOptionPane.WARNING_MESSAGE);
                    } else {
                        cp.adicionarForum(f);
                        this.comunidades.put(cp.getTitulo(), c);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Selecione no m√≠nimo um forum", "Erro",
                        JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Essa comunidade n√£o pode ter foruns",
                    "Erro", JOptionPane.WARNING_MESSAGE);

        }
        preencherListaRelacionamento();
}//GEN-LAST:event_btnAdicionarActionPerformed

