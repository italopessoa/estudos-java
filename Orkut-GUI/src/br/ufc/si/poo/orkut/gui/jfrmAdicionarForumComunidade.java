    /**
     * @author italoney
     * Preencher lista de relacionamentos de acordo com a comunidade selecionada.
     */
    private void preencherListaRelacionamento() {
        Comunidade comunidade = (Comunidade) jcmbComunidade.getSelectedItem();
        DefaultListModel dlm = new DefaultListModel();

        if (comunidade != null) {

            if (comunidade instanceof ComunidadePrivada) {
                ComunidadePrivada cp = (ComunidadePrivada) this.comunidades.get(comunidade.getTitulo());
                for (Forum forum : cp.getForuns()) {
                    dlm.addElement(forum);
                }

                jlistRelacionamento.setModel(dlm);
            } else {
//                JOptionPane.showMessageDialog(null, "Essa comunidade n√£o pode ter foruns",
//                        "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
                dlm = new DefaultListModel();
                jlistRelacionamento.setModel(dlm);
            }
        }
    }

    /**
     * @author italoney
     * Preencher combobox com as comunidades disponiveis para relacionamento.
     */
    private void preencherCombComunidades() {
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();

        for (Comunidade comunidade : this.comunidades.values()) {
            dcbm.addElement(comunidade);
        }

        jcmbComunidade.setModel(dcbm);
    }

    /**
     * @author italoney
     * Preencher lista de foruns disponiveis para relacionamento.
     */
    private void preencherListaForuns() {
        DefaultListModel dlm = new DefaultListModel();
        for (Forum forum : this.foruns.values()) {
            dlm.addElement(forum);
        }

        jlistForum.setModel(dlm);
    }

    /**
     * @author italoney
     * Inicializar campos do focmul√°rio
     */
